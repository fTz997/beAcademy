<?php

//Operadores aritméticos 
//Incremento
$salario = 2000; 
$salarioMaria = 3000;
 
$salario = $salario + 200; // o salário final será de 2200
$salarioMaria = $salarioMaria + 200; // o salário final será de 3200

$salario += 200; // salário = 2400
$salarioMaria += 200; // salário = 3400

// Decremento
$salario -= 600; // salário = 1800
$salario -= 800; // salário = 2600